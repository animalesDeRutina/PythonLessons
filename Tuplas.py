# Tuplas
miTupla = ("juan", 13, 1, 1995)
print(miTupla[2])

# FUNCION LISTA
# list - es una funcion que nos permite transformar una tupla en una lista
#nombreDeLaLista = list(nombreDeLaTupla)

miLista = list(miTupla)
print(miLista)

# FUNCION TUPLE
# Tuple - es una funcion que nos permite convertir una lista en una Tupla
# nombreDeLaTuplaNueva = tuple(nombreDeLaLista)
nuevaTupla = tuple(miLista)
print(nuevaTupla)

